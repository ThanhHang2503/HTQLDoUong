        echo "-- Table: $t\n";
        echo "TRUNCATE TABLE `$t`;\n";
        $rows_r = mysqli_query($conn, "SELECT * FROM `$t`");
        if (!$rows_r || mysqli_num_rows($rows_r) === 0) { echo "\n"; continue; }
        while ($row = mysqli_fetch_assoc($rows_r)) {
            $vals = array_map(function($v) use ($conn) {
                if ($v === null) return 'NULL';
                return "'" . mysqli_real_escape_string($conn, $v) . "'";
            }, array_values($row));
            echo "INSERT INTO `$t` VALUES (" . implode(',', $vals) . ");\n";
        }
        echo "\n";
    }
    echo "SET FOREIGN_KEY_CHECKS=1;\n";
    exit;
}
