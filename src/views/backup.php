$tables = ['accounts','items','invoices','invoice_details','inventory_receipts','inventory_exports',
           'salary_records','leave_requests','resignation_requests','positions','customers','suppliers'];
foreach ($tables as $t) {
    $r = mysqli_query($conn, "SELECT COUNT(*) AS c FROM `$t`");
    $db_stats[$t] = $r ? (int)mysqli_fetch_assoc($r)['c'] : 0;
}

// Lấy DB size
$size_r = mysqli_query($conn, "SELECT ROUND(SUM(data_length + index_length)/1024, 2) AS size_kb
    FROM information_schema.tables WHERE table_schema = DATABASE()");
$db_size = $size_r ? (float)mysqli_fetch_assoc($size_r)['size_kb'] : 0;

// Lấy ngày tạo lần đầu (account đầu tiên)
$oldest_r = mysqli_query($conn, "SELECT MIN(created_at) AS oldest FROM accounts");
